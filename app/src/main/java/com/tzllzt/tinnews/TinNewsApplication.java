package com.tzllzt.tinnews;

//**********************************************************************************************************************
// * Includes
//**********************************************************************************************************************

// Project includes
import com.tzllzt.tinnews.database.TinNewsDatabase;

// Framework includes
import android.app.Application;
import androidx.room.Room;

//**********************************************************************************************************************
// * Class definition
//**********************************************************************************************************************
public class TinNewsApplication extends Application {

//**********************************************************************************************************************
// * Public methods
//**********************************************************************************************************************
    @Override
    public void onCreate() {
        super.onCreate();
        database = Room.databaseBuilder(this, TinNewsDatabase.class, "tinnews_db").build();
    }

    public static TinNewsDatabase getDatabase() {
        return database;
   }

//**********************************************************************************************************************
// * Private attributes
//**********************************************************************************************************************
    private static TinNewsDatabase database;

}

